class Datatype {
	public static void main(String [] datatype) {
		


			boolean b = true;
			char    c = 'S';
			short   s = 10;
 			long    l = 2000;
			float   f = 3.675f;
			double  d = 8.987;
			int     i = 30;


		System.out.println( b + " is Boolean   Datatype " ); 
		System.out.println( c + " is character Datatype " );
		System.out.println( s + " is short     Datatype " );
		System.out.println( l + " is long      Datatype " );
		System.out.println( f + " is float     Datatype " );
		System.out.println( d + " is double    Datatype " );
		System.out.println( i + " is integer   Datatype " );
	}
}	